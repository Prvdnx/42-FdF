#include "libft.h"

char	*ft_strcpy(char *dest, const char *s1)
{
	int	i;

	i = 0;
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
